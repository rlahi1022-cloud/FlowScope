#pragma once

// ------------------------------------------------
// epollserver4.h
// server4 epoll ET 이벤트 루프 서버 정의
//
// 핵심 원칙:
//   1. epoll ET 모드: 이벤트 발생 시 EAGAIN까지 루프로 읽어야 함
//   2. 모든 소켓은 non-blocking
//   3. epoll 스레드가 router4 호출
//   4. router4가 dispatcher 통해 처리 경로 결정
//     - sync: 핸들러 직접 호출
//     - async: jobqueue4에 push
//     - event: eventbus4에 publish
// ------------------------------------------------

#include <map>
#include <string>
#include <atomic>

// ------------------------------------------------
// 상수 정의
// ------------------------------------------------
static constexpr int EPOLL_MAX_EVENTS4 = 1024;

// ------------------------------------------------
// epollserver4
// ------------------------------------------------
class epollserver4 {
public:
    // ------------------------------------------------
    // 생성자
    // ------------------------------------------------
    explicit epollserver4(int port);

    // ------------------------------------------------
    // 소멸자
    // ------------------------------------------------
    ~epollserver4();

    // ------------------------------------------------
    // run
    // 서버 메인 루프
    // ------------------------------------------------
    void run();

    // ------------------------------------------------
    // stop
    // 이벤트 루프 종료 요청
    // ------------------------------------------------
    void stop();

private:
    // ------------------------------------------------
    // init_listen_socket
    // 리슨 소켓 생성 및 설정
    // ------------------------------------------------
    bool init_listen_socket();

    // ------------------------------------------------
    // init_epoll
    // epoll 인스턴스 생성
    // ------------------------------------------------
    bool init_epoll();

    // ------------------------------------------------
    // add_to_epoll
    // fd를 epoll에 등록 (ET 모드)
    // ------------------------------------------------
    bool add_to_epoll(int fd, uint32_t events);

    // ------------------------------------------------
    // remove_from_epoll
    // fd를 epoll에서 제거
    // ------------------------------------------------
    void remove_from_epoll(int fd);

    // ------------------------------------------------
    // set_nonblocking
    // fd를 O_NONBLOCK으로 설정
    // ------------------------------------------------
    bool set_nonblocking(int fd);

    // ------------------------------------------------
    // handle_accept
    // 새 클라이언트 연결 처리
    // ------------------------------------------------
    void handle_accept();

    // ------------------------------------------------
    // handle_read
    // 클라이언트 데이터 수신
    // ------------------------------------------------
    void handle_read(int fd);

    // ------------------------------------------------
    // handle_close
    // 클라이언트 연결 종료 처리
    // ------------------------------------------------
    void handle_close(int fd);

    // ------------------------------------------------
    // process_buffer
    // 수신 버퍼에서 완성된 패킷을 추출해 router4로 전달
    // ------------------------------------------------
    void process_buffer(int fd);

    int                      port_;            // 리슨 포트
    int                      listenfd_;        // 리슨 소켓 fd
    int                      epollfd_;         // epoll 인스턴스 fd
    std::atomic<bool>        running_;         // 실행 상태 플래그
    std::map<int, std::string> recvbufs_;      // fd별 수신 버퍼
};
