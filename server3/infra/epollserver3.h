#pragma once

// ------------------------------------------------
// epollserver3.h
// server3 epoll ET 이벤트 루프 서버 헤더
//
// 핵심 원칙:
//   1. epoll ET 모드: 이벤트 발생 시 EAGAIN까지 루프로 읽어야 함
//   2. 모든 소켓은 non-blocking
//   3. epoll 스레드가 router3 호출 (router → eventbus.publish)
//   4. EventBus로 handler와 service 통신
// ------------------------------------------------

#include "router/router3.h"
#include "common/packet.h"

#include <sys/epoll.h>
#include <unordered_map>
#include <string>
#include <atomic>

// ------------------------------------------------
// epoll 최대 동시 이벤트 개수
// ------------------------------------------------
static constexpr int EPOLL_MAX_EVENTS3 = 64;

// ------------------------------------------------
// epollserver3
// epoll ET 기반 비동기 이벤트 루프 서버
// ------------------------------------------------
class epollserver3
{
public:
    // ------------------------------------------------
    // 생성자
    // port : 리슨 포트 번호
    // ------------------------------------------------
    explicit epollserver3(int port);

    // ------------------------------------------------
    // 소멸자
    // 리슨/epoll fd 정리
    // ------------------------------------------------
    ~epollserver3();

    // ------------------------------------------------
    // run
    // 서버 메인 이벤트 루프 시작
    // 1. 리슨 소켓 초기화
    // 2. epoll 초기화
    // 3. epoll_wait 루프 (이벤트 감지 → 처리)
    // ------------------------------------------------
    void run();

    // ------------------------------------------------
    // stop
    // 이벤트 루프 종료 요청
    // ------------------------------------------------
    void stop();

private:
    // ------------------------------------------------
    // init_listen_socket
    // 리슨 소켓 생성 → bind → listen → non-blocking 설정
    // ------------------------------------------------
    bool init_listen_socket();

    // ------------------------------------------------
    // init_epoll
    // epoll 인스턴스 생성
    // ------------------------------------------------
    bool init_epoll();

    // ------------------------------------------------
    // set_nonblocking
    // fd를 non-blocking 모드로 설정
    // ------------------------------------------------
    bool set_nonblocking(int fd);

    // ------------------------------------------------
    // add_to_epoll
    // fd를 epoll ET 모드로 등록
    // ------------------------------------------------
    bool add_to_epoll(int fd, uint32_t events);

    // ------------------------------------------------
    // remove_from_epoll
    // fd를 epoll에서 제거
    // ------------------------------------------------
    void remove_from_epoll(int fd);

    // ------------------------------------------------
    // handle_accept
    // 새 클라이언트 연결 처리
    // ET 모드: EAGAIN까지 루프로 accept
    // ------------------------------------------------
    void handle_accept();

    // ------------------------------------------------
    // handle_read
    // 클라이언트 데이터 수신
    // ET 모드: EAGAIN이 나올 때까지 루프로 읽음
    // ------------------------------------------------
    void handle_read(int fd);

    // ------------------------------------------------
    // handle_close
    // 클라이언트 연결 종료 처리
    // epoll 제거 → 버퍼 제거 → fd 닫기
    // ------------------------------------------------
    void handle_close(int fd);

    // ------------------------------------------------
    // process_buffer
    // 수신 버퍼에서 완성된 패킷을 추출해 router3로 전달
    // 패킷 구조: [4byte 빅엔디언 bodylen][body]
    // ------------------------------------------------
    void process_buffer(int fd);

    int                              port_;          // 리슨 포트
    int                              listenfd_;      // 리슨 소켓 fd
    int                              epollfd_;       // epoll fd
    std::atomic<bool>                running_;       // 실행 상태
    router3                          router_;        // 라우터 (router → eventbus.publish)
    std::unordered_map<int, std::string> recvbufs_; // 클라이언트별 수신 버퍼
};
