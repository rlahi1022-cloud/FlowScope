#pragma once

// ------------------------------------------------
// epollserver.h
// epoll 기반 non-blocking TCP 서버
// Edge Triggered 모드 사용
// epoll 스레드는 절대 blocking 작업을 하지 않는다
//
// 흐름:
//   accept → EPOLLIN 등록 →
//   데이터 수신 → router.route() 호출
// ------------------------------------------------

#include "router/router.h"
#include <string>
#include <unordered_map>
#include <vector>

// ------------------------------------------------
// epollserver
// non-blocking epoll 기반 TCP 서버
// ------------------------------------------------
class epollserver
{
public:
    // ------------------------------------------------
    // 생성자
    // port: 수신 대기 포트
    // ------------------------------------------------
    explicit epollserver(int port);

    // ------------------------------------------------
    // 소멸자: fd 정리
    // ------------------------------------------------
    ~epollserver();

    // ------------------------------------------------
    // run
    // epoll 이벤트 루프 시작 (블로킹)
    // ------------------------------------------------
    void run();

    // ------------------------------------------------
    // stop
    // 서버 종료 플래그 설정
    // ------------------------------------------------
    void stop();

private:
    // ------------------------------------------------
    // init_listen_socket
    // 리슨 소켓 생성 및 non-blocking 설정
    // ------------------------------------------------
    bool init_listen_socket();

    // ------------------------------------------------
    // set_nonblocking
    // fd를 non-blocking 모드로 전환
    // ------------------------------------------------
    bool set_nonblocking(int fd);

    // ------------------------------------------------
    // add_epoll_fd
    // epoll 이벤트 등록 (EPOLLIN | EPOLLET)
    // ------------------------------------------------
    bool add_epoll_fd(int fd);

    // ------------------------------------------------
    // handle_accept
    // 새 클라이언트 연결 수락
    // ------------------------------------------------
    void handle_accept();

    // ------------------------------------------------
    // handle_read
    // 클라이언트 데이터 수신 및 패킷 파싱
    // Edge Triggered: EAGAIN까지 반복 읽기
    // ------------------------------------------------
    void handle_read(int fd);

    // ------------------------------------------------
    // close_client
    // 클라이언트 연결 종료 및 정리
    // ------------------------------------------------
    void close_client(int fd);

    // ------------------------------------------------
    // parse_packet
    // 4바이트 헤더 + JSON 바디 파싱
    // 완성된 패킷이 있으면 router로 전달
    // ------------------------------------------------
    void parse_packet(int fd);

    int port_;          // 수신 대기 포트
    int listenfd_;      // 리슨 소켓 fd
    int epollfd_;       // epoll 인스턴스 fd
    bool running_;      // 서버 실행 플래그

    // 클라이언트별 수신 버퍼
    // fd → 수신 버퍼 (부분 패킷 누적용)
    std::unordered_map<int, std::vector<char>> recvbuffers_;

    // router 인스턴스
    router router_;
};